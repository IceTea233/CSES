#include<bits/stdc++.h>
#define LLI long long int
#define pii pair<int,int>
#define fst first
#define sec second
#define ULTRA 2134567890
#define de(x) if(x!=0 && MODE==x)
#define MODE 1
using namespace std;

int main() {

    int i,j;
    int n;

    cin >> n;

    vector<pii> keep;
    for( i=1; i<=n; i++) {
        int a, d;
        cin >> a >> d;
        keep.push_back( { a, d});
    }
    sort( begin(keep), end(keep));

    LLI ans = 0;
    LLI now = 0;
    for( auto it:keep) {
        now += it.fst;
        ans += it.sec - now;
    }

    cout << ans << "\n";


    return 0;
}

