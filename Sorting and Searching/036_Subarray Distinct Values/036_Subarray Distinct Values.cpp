int x[maxn] = {};

int32_t main() {
    int i,j;
    int n,k;

    cin >> n >> k;
    for (int i=1; i<=n; i++)
        cin >> x[i];
    int cnt = 0;
    map<int,int> mp;

    int ans = 0;
    for (int i=1, j=0; i<=n; i++) {
        while (j < n && (cnt < k || cnt == k && mp[x[j+1]])) {
            if (!(mp[x[++j]]++))
                cnt++;
        }
        ans += j-i+1;
        // printf("(%lld, %lld)\n", i, j);

        if (--mp[x[i]] == 0)
            cnt--;
    }
    printf("%lld\n", ans);

    return 0;
}

