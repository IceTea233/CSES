using namespace std;

int dp[1000007] = {};

int main() {

    int i,j;
    int n;

    cin >> n;

    for( i=0; i<n; i++) {
        dp[i] = ULTRA;
    }
    dp[n] = 0;
    for( i=n; i>0; i--) {
        int num = i;
        while(num) {
            int digit = num % 10;
            dp[ i-digit ] = min( dp[ i-digit], dp[i]+1);
            num /= 10;
        }
    }

    cout << dp[0] << "\n";

    return 0;
}

